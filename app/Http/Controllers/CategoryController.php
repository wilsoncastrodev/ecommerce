    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'category_title' => 'required|unique:categories|max:255',
            'category_top' => 'required',
            'category_image' => 'required|image|mimes:jpg,png,jpeg,gif,svg|max:2048'
        ]);

        $image = $request->file('category_image');
        $file_name = Str::slug($request->category_title) . '_' . time() . '.' . $image->getClientOriginalExtension();
        $path = $image->storeAs('public/imagens/categorias', $file_name);

        $category = new Category;
        $category->category_title = $request->category_title;
        $category->category_top = $request->category_top;
        $category->category_image = $path;
        $category->save();
        
        return redirect()->back()->with('success', 'Categoria cadastrada com sucesso!');
    }

